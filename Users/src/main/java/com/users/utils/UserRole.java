package com.users.utils;

/**
 * Enumeration representing the roles that a user can have in the system.
 * <p>
 * This enum defines the possible user roles, including:
 * <ul>
 *   <li>{@code CUSTOMER} - Represents a regular customer in the system.</li>
 *   <li>{@code RESTAURANT_OWNER} - Represents a restaurant owner in the system.</li>
 * </ul>
 */
public enum UserRole {
  /**
   * Represents a regular customer in the system.
   */
  CUSTOMER,

  /**
   * Represents a restaurant owner in the system.
   */
  RESTAURANT_OWNER
}
